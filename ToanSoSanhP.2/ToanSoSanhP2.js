/**
 * Toán tử so sánh
 *  === 
 * !==
 */

/**
 * sự khác nhau giữa == và ===
 * toán tử == thì nó chỉ so sanh kiểu value thôi
 * còn === thì  thì nó so sánh cả hai , Value và kiểu dữ liệu ,nó sẽ chặt chẻ hơn ==
 */
var a=2;
var b='2';
console.log(a===b)

/**
 * sự khác nhau giửa != và !==
 * toán tử != thì nó chỉ quan tâm đến so sanh kiểu value thôi
 * còn !== thì thì nó so sánh cả hai , Value và kiểu dữ liệu ,nó sẽ chặt chẻ hơn !=
 */

 var a=2;
 var b='2';
 console.log(a!==b)
